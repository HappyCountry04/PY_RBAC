create table sys_dept (
  dept_id bigint generated by default as identity (start with 200) primary key,
  parent_id bigint not null default 0,
  ancestors varchar(200) not null default '',
  dept_name varchar(30) not null default '',
  order_num int not null default 0,
  leader varchar(20),
  phone varchar(11),
  email varchar(50),
  status char(1) not null default '0',
  del_flag char(1) not null default '0',
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp
);

create table sys_user (
  user_id bigint generated by default as identity (start with 100) primary key,
  dept_id bigint references sys_dept(dept_id),
  user_name varchar(30) not null unique,
  nick_name varchar(30) not null,
  user_type varchar(2) not null default '00',
  email varchar(50) default '',
  phonenumber varchar(11) default '',
  sex char(1) default '0',
  avatar varchar(100) default '',
  password varchar(100) default '',
  status char(1) not null default '0',
  del_flag char(1) not null default '0',
  login_ip varchar(128) default '',
  login_date timestamp,
  pwd_update_date timestamp,
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500)
);

create table sys_role (
  role_id bigint generated by default as identity (start with 100) primary key,
  role_name varchar(30) not null,
  role_key varchar(100) not null unique,
  role_sort int not null,
  data_scope char(1) not null default '1',
  menu_check_strictly boolean not null default true,
  dept_check_strictly boolean not null default true,
  status char(1) not null default '0',
  del_flag char(1) not null default '0',
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500)
);

create table sys_menu (
  menu_id bigint generated by default as identity (start with 2000) primary key,
  menu_name varchar(50) not null,
  parent_id bigint not null default 0,
  order_num int not null default 0,
  path varchar(200) not null default '',
  component varchar(255),
  query varchar(255),
  route_name varchar(50) default '',
  is_frame int not null default 1,
  is_cache int not null default 0,
  menu_type char(1) not null default '',
  visible char(1) not null default '0',
  status char(1) not null default '0',
  perms varchar(100),
  icon varchar(100) default '#',
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500) default ''
);

create table sys_post (
  post_id bigint generated by default as identity primary key,
  post_code varchar(64) not null,
  post_name varchar(50) not null,
  post_sort int not null,
  status char(1) not null,
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500)
);

create table sys_user_role (
  user_id bigint not null references sys_user(user_id) on delete cascade,
  role_id bigint not null references sys_role(role_id) on delete cascade,
  primary key(user_id, role_id)
);

create table sys_role_menu (
  role_id bigint not null references sys_role(role_id) on delete cascade,
  menu_id bigint not null references sys_menu(menu_id) on delete cascade,
  primary key(role_id, menu_id)
);

create table sys_role_dept (
  role_id bigint not null references sys_role(role_id) on delete cascade,
  dept_id bigint not null references sys_dept(dept_id) on delete cascade,
  primary key(role_id, dept_id)
);

create table sys_user_post (
  user_id bigint not null references sys_user(user_id) on delete cascade,
  post_id bigint not null references sys_post(post_id) on delete cascade,
  primary key(user_id, post_id)
);

create table sys_config (
  config_id bigint generated by default as identity primary key,
  config_name varchar(100) default '',
  config_key varchar(100) not null unique,
  config_value varchar(500) default '',
  config_type char(1) default 'N',
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500)
);

create table sys_dict_type (
  dict_id bigint generated by default as identity primary key,
  dict_name varchar(100) default '',
  dict_type varchar(100) not null unique,
  status char(1) not null default '0',
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500)
);

create table sys_dict_data (
  dict_code bigint generated by default as identity primary key,
  dict_sort int not null default 0,
  dict_label varchar(100) default '',
  dict_value varchar(100) default '',
  dict_type varchar(100) default '',
  css_class varchar(100),
  list_class varchar(100),
  is_default char(1) default 'N',
  status char(1) not null default '0',
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500)
);

create table sys_oper_log (
  oper_id bigint generated by default as identity primary key,
  title varchar(50) default '',
  business_type int default 0,
  method varchar(200) default '',
  request_method varchar(10) default '',
  oper_name varchar(50) default '',
  oper_url varchar(255) default '',
  oper_ip varchar(128) default '',
  oper_param varchar(2000) default '',
  json_result varchar(2000) default '',
  status int default 0,
  error_msg varchar(2000) default '',
  oper_time timestamp,
  cost_time bigint default 0
);

create table sys_logininfor (
  info_id bigint generated by default as identity primary key,
  user_name varchar(50) default '',
  ipaddr varchar(128) default '',
  login_location varchar(255) default '',
  browser varchar(50) default '',
  os varchar(50) default '',
  status char(1) default '0',
  msg varchar(255) default '',
  login_time timestamp
);

